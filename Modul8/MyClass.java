// Eksempel på før refactoring
public class MyClass {
  public void myMethod() {
    int a = 5; // Deklarerer en heltallsvariabel og gir den verdien 5
    int b = 10; // Deklarerer en annen heltallsvariabel og gir den verdien 10
    int c = a + b; // Legger sammen verdiene av a og b og lagrer resultatet i en ny heltallsvariabel kalt c
    System.out.println(c); // Skriver ut verdien av c til konsollen
  }
}

  
