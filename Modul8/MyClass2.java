// Eksempel på etter refactoring 
public class MyClass2 {
  public void myMethod() {
    int a = 5; // Deklarerer en heltallsvariabel og gir den verdien 5
    int b = 10; // Deklarerer en annen heltallsvariabel og gir den verdien 10
    System.out.println(a + b); // Legger sammen verdiene av a og b og skriver ut resultatet til konsollen som hadde vært "c"
  }
}

  
